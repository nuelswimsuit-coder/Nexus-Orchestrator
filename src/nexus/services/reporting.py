"""
ReportingService — Automated Profit Report Generator.

Aggregates operational data from telefix.db and delivers structured
reports via Telegram at three scheduled times per day:
  09:00 → Morning briefing (last 9 hours)
  14:00 → Afternoon pulse   (last 5 hours)
  21:00 → Evening profit report (last 24 hours, full stats)

Report contents
---------------
  📊 NEXUS REPORT — EVENING
  ─────────────────────────────
  📅 Date: 2026-03-19  |  Period: Last 24 hours

  💰 REVENUE METRICS
  • New users scraped (24h): 87
  • Total pipeline users:    223
  • Active target groups:    35
  • Estimated ROI:           +64%

  📈 TRADING PERFORMANCE (Paper Mode)
  • Virtual PnL:     +$4.20
  • Win Rate:        67%  (8W / 4L)
  • Auto-Restarts:   2

  🏆 PEAK OPPORTUNITY
  • Max arbitrage gap identified: 8.3%
  • Timestamp: 14:22 UTC

  🤖 SESSION HEALTH
  • Active sessions:  50  ✅
  • Frozen sessions:  44  ⚠️
  • Health ratio:     53%

  ⚡ NODE STATUS
  • Master:  Online ✅
  • Workers: 2 active

  🔗 Dashboard: http://localhost:8001/nexus-os
  ─────────────────────────────
  Generated by Nexus Orchestrator

Report Schedule
---------------
Three times per day — configured via MultiReportingService:
  report_times = [(9, 0, "morning"), (14, 0, "afternoon"), (21, 0, "evening")]

RGB Signal
----------
When a report is being sent, the master writes "nexus:report:sending" to
Redis with a 10-second TTL.  The dashboard polls this key and flashes the
Master PC RGB Neon Blue for the duration.
"""

